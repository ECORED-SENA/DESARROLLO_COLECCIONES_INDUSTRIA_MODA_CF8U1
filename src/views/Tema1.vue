        p El patronaje en el vestuario es el proceso por el cual se realiza un patrón, molde o plantilla de una prenda de vestir, partiendo de una adecuada toma de medidas o manejo de tabla de medidas. De esta manera, se realizan unos planos de cada pieza que conforma la prenda de vestir y que pueden replicarse infinitas veces, partiendo del mismo patrón.
        p La palabra patronaje viene de la unión de la palabra «patrón», que proviene del sustantivo padre, y el sufijo «aje» que indica una acción. El sustantivo patro/patrón se utiliza para designar un molde o prototipo que realiza la función de padre, para reproducir otros ejemplares a partir de él.
        p Dichos patrones son los que permiten la construcción de prendas de vestir, siguiendo las diferentes morfologías del cuerpo, que se adapten a la antropometría y biomecánica de este, para que se acople tanto a la silueta, como a los movimientos, haciendo que estas prendas sean funcionales.
        p Al principio, las prendas de vestir se construían a partir de envolver el cuerpo en materiales como pieles o tejidos en telares. Después se empezaron a utilizar telas que se cortaban para ir adaptándose al cuerpo. Los primeros patrones que se conocen son de mangas ya que necesitaban que fueran iguales para los dos brazos.
        p El patronaje fue un saber que se pasaba de generación en generación en las familias, como secreto familiar. No se popularizó el patronaje sino hasta finales del siglo XIX, donde por medio de revistas, catálogos y cartillas que llegaban a los hogares, todas las familias tuvieron acceso a patronajes de vestuario. El primer libro de patronaje que se conoce se llama “libro de geometría práctica y trazo” escrito por Juan Alcega en 1580.

        p El patronaje femenino nació después del patronaje masculino, dichos patronajes, aunque surgen del mismo entendimiento y análisis del cuerpo, son diferentes; aunque estructuralmente la mujer y el hombre son iguales, morfológicamente son distintos. 

        p El patronaje masculino cubre un cuerpo que estructuralmente es sólido, mientras que el patronaje femenino cubre un cuerpo que posee muchas estructuras blandas, como por ejemplo los senos.        
